#include <stdio.h>

/**
 * add - adds two numbers
 * @a: number
 * @b: number
 * Return: sum of numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: number
 * @b: number
 * Return: difference of numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: number
 * @b: number
 * Return: multiplication of numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: number
 * @b: number
 * Return: division of numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modules two numbers
 * @a: number
 * @b: number
 * Return: module of numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
